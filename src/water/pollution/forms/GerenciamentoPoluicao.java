    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String SQL = ""; 
        for(RioPoluicao riop : this.Dados){
            try{
                switch(riop.Acao){
                    case "I":
                        /** Monta o Insert do RioPoluição */ 
                        SQL = "INSERT INTO RioPoluicao (FK_RIO, FK_TIPOPOLUICAO, Ano, Quantidade) VALUES ( ";
                        SQL += "    '" + riop.Fk_rio.toString() + "' ,";
                        SQL += "    '" + riop.Fk_tipopoluicao.toString() + "' ,";
                        SQL += "    '" + riop.Ano.toString() + "' ,";
                        SQL += "    '" + riop.Quantidade.toString() + "'  )";
                        PreparedStatement pstm = ConexaoDB.getNewPreparedStatement(SQL);
                        
                        pstm.execute();
                        pstm.close();
                        break;

                    case "A":
                        /** Monta o Update do RioPoluição */ 
                        String query = "UPDATE RIOPOLUICAO SET FK_RIO = ?, FK_TIPOPOLUICAO = ?, Ano = ?, Quantidade = ? WHERE _ID = ?";
                        pstm = ConexaoDB.getNewPreparedStatement(query);
                        
                        pstm.setInt(1, riop.Fk_rio);
                        pstm.setInt(2, riop.Fk_tipopoluicao);
                        pstm.setInt(3, riop.Ano);
                        pstm.setFloat(4, riop.Quantidade);
                        pstm.setInt(5, riop._Id);
                        
                        pstm.execute();
                        pstm.close();
                        break;

                    case "D":
                        /** Monta o Delete do RioPoluição */ 
                        SQL = "DELETE FROM RioPoluicao WHERE _ID = ?";
                        
                        pstm = ConexaoDB.getNewPreparedStatement(SQL);
                        pstm.setInt(1, riop._Id);
                        
                        pstm.execute();
                        pstm.close();
                        break;
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro na ação '"+ riop.Acao +"' da poluição.");
                return;
            }
        }
        
        JOptionPane.showMessageDialog(null, "Alterações salvas com sucesso.");
    }//GEN-LAST:event_jButton2MouseClicked

