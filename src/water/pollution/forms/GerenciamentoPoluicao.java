    private void AtualizarJTableFromDados(){
        DefaultTableModel modeloTabela = (DefaultTableModel) jTableRios.getModel();
            
        // Limpando registros
        modeloTabela.setRowCount(0);
        
        for(RioPoluicao pol : this.Dados){
            modeloTabela.addRow(new Object[] {  
                pol._Id,
                pol.Fk_rio,
                pol.NomeRio,
                pol.Fk_tipopoluicao, 
                pol.NomeTipoPoluicao,
                pol.Ano,
                pol.Quantidade
            }); 
        }
        
        jTableRios.setModel(modeloTabela);
    }
    
