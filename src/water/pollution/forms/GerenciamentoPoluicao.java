    private void btn_LocalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_LocalizarMouseClicked
        // PEga um rio qualquer

        try {
            PreparedStatement query = ConexaoDB.getNewPreparedStatement("SELECT * FROM VW_RIOPOLUICAO");
            ResultSet result = query.executeQuery();

            /** Montando JTable */
            DefaultTableModel modeloTabela = (DefaultTableModel) jTableRios.getModel();

            // Limpando registros
            modeloTabela.setRowCount(0);
            this.Dados.clear();

            while(result.next()) {
                modeloTabela.addRow(new Object[] {
                    String.valueOf(result.getInt("_ID")),
                    String.valueOf(result.getInt("IdRio")),
                    result.getString("NomeRio"),
                    String.valueOf(result.getInt("IdTipoPoluicao")),
                    result.getString("NomeTipoPoluicao"),
                    String.valueOf(result.getInt("Ano")),
                    result.getDouble("Quantidade")
                });

                RioPoluicao newDado = new RioPoluicao();
                newDado._Id = result.getInt("_ID");
                newDado.Fk_rio = result.getInt("IdRio");
                newDado.NomeRio = result.getString("NomeRio");
                newDado.Fk_tipopoluicao = result.getInt("IdTipoPoluicao");
                newDado.NomeTipoPoluicao = result.getString("NomeTipoPoluicao");
                newDado.Ano = result.getInt("Ano");
                newDado.Quantidade = result.getFloat("Quantidade");
                newDado.Acao = "";
                this.Dados.add(newDado);
            }

            jTableRios.setModel(modeloTabela);
            result.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar poluições.\n\n" + ex.getMessage());
        }
    }//GEN-LAST:event_btn_LocalizarMouseClicked

