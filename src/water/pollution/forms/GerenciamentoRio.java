    private void btn_LocalizarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_LocalizarMouseClicked
        // PEga um rio qualquer

        try {
            // Essa é uma forma de realizar uma select
            //      
            
            // Essa é outra de realizar qualquer query (INSET, UPDATE, DELETE, SELECT)
            // podendo trocar parametros por valore para evitar SQLInject
            // OBS: Quando for INSER, DELETE ou UPDATE é importante fechar depois (query.close())
            
            String SQL, valorTroca = null;
            
            SQL = "SELECT * FROM RIO ";
            
            switch (ComboBoxFiltro.getSelectedIndex()) {
                case 0:
                    SQL += "";
                    valorTroca = "Tudo";
                    break;
                case 1:
                    SQL += " WHERE Nome = ?";
                    valorTroca = txt_Busca.getText();
                    break;
                case 2:
                    SQL += " WHERE _ID = ? ";
                    valorTroca = txt_Busca.getText();
                    break;
                default:
                    break;
            }
              
            PreparedStatement query = ConexaoDB.getNewPreparedStatement(SQL);
            
            if(valorTroca != "Tudo"){
                query.setString(1, valorTroca);
            }
            
            
            ResultSet result = query.executeQuery();
            
            if(result == null){
                return;
            }
            
            /** Montando JTable */
            DefaultTableModel modeloTabela = (DefaultTableModel) jTableRios.getModel();
            
            // Limpando registros
            modeloTabela.setRowCount(0);
            this.Dados.clear();

            while(result.next()) {
                modeloTabela.addRow(new String[] {  
                        String.valueOf(result.getInt("_ID")),
                        result.getString("Nome"),
                        result.getString("Localidade"),
                        result.getString("Nascente"),
                        
                    }); 
                
                Rio newRio = new Rio();
                newRio.id = result.getInt("_ID");
                newRio.Nome = result.getString("Nome");                
                newRio.Localidade = result.getString("Localidade");
                newRio.Nascente = result.getString("Nascente");
                newRio.Acao = "";
                this.Dados.add(newRio);
            }

            jTableRios.setModel(modeloTabela);
            result.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar rio.\n\n" + ex.getMessage());
        }
        
        
        
    }//GEN-LAST:event_btn_LocalizarMouseClicked

    private void AtualizarJTableFromDados(){
        DefaultTableModel modeloTabela = (DefaultTableModel) jTableRios.getModel();
            
        // Limpando registros
        modeloTabela.setRowCount(0);
        
        for(Rio rio : this.Dados){
            modeloTabela.addRow(new String[] {
                String.valueOf(rio.id),
                rio.Nome,
                rio.Localidade,
                rio.Nascente,
            });
        }
        
        jTableRios.setModel(modeloTabela);
    }
    
