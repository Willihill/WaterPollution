    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String SQL = ""; 
        for(Rio rio : this.Dados){
            try{
                
                switch(rio.Acao){
                    case "I":
                        /** Monta o Insert do Rio */ 
                        SQL = "INSERT INTO Rio (Nome, Localidade, Nascente) VALUES ( ";
                        SQL += "    '" + rio.Nome + "' ,";
                        SQL += "    '" + rio.Localidade + "' ,";
                        SQL += "    '" + rio.Nascente + "'  )";
                        PreparedStatement pstm = ConexaoDB.getNewPreparedStatement(SQL);
//                        pstm.setString(1, rio.Nome);
//                        pstm.setString(2, rio.Localidade);
//                        pstm.setString(3, rio.Nascente);
                        pstm.execute();
                        pstm.close();
                        break;

                    case "A":
                        /** Monta o Update do Rio */ 
                        SQL = "UPDATE Rio";
                        SQL += "    SET Nome = '" + rio.Nome + "'";
                        SQL += "       ,Localidade = '" + rio.Localidade + "'";
                        SQL += "       ,Nascente = '" + rio.Nascente + "'";
                        SQL += "    WHERE Rio._ID = ?";
                        pstm = ConexaoDB.getNewPreparedStatement(SQL);
//                        pstm.setString(1, rio.Nome);                        
//                        pstm.setString(2, rio.Localidade);
//                        pstm.setString(3, rio.Nascente);
                        pstm.setInt(1, rio.id);
                        pstm.execute();
                        pstm.close();
                        break;

                    case "D":
                        /** Monta o Delete do Rio */ 
                        SQL = "DELETE FROM Rio ";
                        SQL += "WHERE _ID = ?";
                        pstm = ConexaoDB.getNewPreparedStatement(SQL);
                        pstm.setInt(1, rio.id);
                        pstm.execute();
                        pstm.close();
                        break;
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro na ação '"+ rio.Acao +"' do rio "
                        + rio.id + " - " + rio.Nome + ". Motivo: " + e.getMessage());
                return;
            }
        }
        
        JOptionPane.showMessageDialog(null,"Alterações salvas com sucesso.");
    }//GEN-LAST:event_jButton2MouseClicked

